package com.patternPrint;

public class EvenOddExample {

	public static void main(String[] args) {
		for(int a = 1; a <= 10; a++)
	      {
	         if(a % 2 == 0)
	         {
	            System.out.println(a +":"+ "Even Number ");
	         }else {
				System.out.println(a+":"+"odd Number");
			}
	      }
	     
	}

}
